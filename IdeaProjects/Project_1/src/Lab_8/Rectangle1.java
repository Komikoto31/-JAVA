package Lab_8;

import java.awt.*;

// Класс для прямоугольника
class Rectangle extends Shape1 {
    private int width, height;

    public Rectangle(int x, int y, int width, int height) {
        super(x, y);
        this.width = width;
        this.height = height;
    }

    @Override
    void draw(Graphics g) {
        g.setColor(color);
        g.fillRect(x, y, width, height);
    }

}
