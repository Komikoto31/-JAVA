package Lab_10;
import java.util.Scanner;

public class Test_16 {
    public static void main(String[] args) {
        // Создаем объект Scanner для считывания данных с клавиатуры
        Scanner scanner = new Scanner(System.in);

        // Инициализируем переменные для хранения максимального значения и счетчика
        int max = 0; // Максимальное значение
        int count = 0; // Счетчик чисел, равных максимальному

        // Начинаем бесконечный цикл для считывания чисел из ввода
        while (true) {
            // Считываем следующее число из ввода
            int num = scanner.nextInt();

            // Проверяем, если введено число 0, то завершаем цикл
            if (num == 0) {
                break; // Выход из цикла
            }

            // Если текущее число больше текущего максимума
            if (num > max) {
                max = num; // Обновляем максимальное значение
                count = 1; // Сбрасываем счетчик на 1, так как у нас новый максимум
            } else if (num == max) {
                // Если текущее число равно текущему максимуму, увеличиваем счетчик
                count++;
            }
        }

        // После завершения цикла выводим количество чисел, равных максимуму
        System.out.println(count);
    }
}